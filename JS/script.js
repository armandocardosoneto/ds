
//Exercício 5
var digitado = prompt('Digite um numero de 1 a 5.');

if(digitado == 1){
    alert('Numero um digitado.');
}
else if(digitado == 2){
    alert('Numero dois digitado.');
}
else if(digitado == 3){
    alert('Numero três digitado.');
}
else if(digitado == 4){
    alert('Numero quatro digitado.');
}
else {
    alert('Numero cinco digitado.');
}